

public class operators1 {
    
public static void main(String args[])
{
    int a=10;
    int b=5;
    //Arithmetic Operators
    //Binary Operators 
    System.out.println(a+b);
    System.out.println(a-b);
    System.out.println(a*b);
    System.out.println(a/b);
    System.out.println(a%b);
    //Unary Operators
    System.out.println(a++);
    System.out.println(b--);
    System.out.println(a);
    System.out.println(b);
    //Relational Operators
    System.out.println(a==b);
    System.out.println(a!=b);
    System.out.println(a>b);
    System.out.println(a<b);
    System.out.println(a>=b);
    System.out.println(a<=b);
    //Logical operators
    System.out.println(a<=b && a>b);
    System.out.println(a<=b || a>b);
    System.out.println(!(a<=b));
    //Assignment operator
   
    a+=5;
    System.out.println(a);
    a-=5;
    System.out.println(a);
    a*=5;
    System.out.println(a);
    a/=5;
    System.out.println(a);
    a%=5;
    System.out.println(a);

    //Bitwise Operator


    

}

}
